/**
 * @file s21_negate.c
 * @brief Реализация функции изменения знака decimal числа
 * @details Изменяет знак числа на противоположный
 */

#include "s21_decimal.h"

/**
 * @brief Изменить знак числа на противоположный
 * @param value Исходное число
 * @param result Указатель для сохранения результата
 * @return 0 при успехе, 1 при ошибке (некорректный указатель)
 * @details Копирует исходное число и инвертирует его знак
 *          Положительное число становится отрицательным и наоборот
 */
int s21_negate(s21_decimal value, s21_decimal* result) {
  if (result) {
    // Копируем исходное число
    *result = value;
    // Инвертируем знак: 0 → 1, 1 → 0
    s21_set_sign(result, !s21_get_sign(result));
  }

  // Возвращаем код ошибки: 0 если result корректен, 1 если NULL
  return !result;
}
